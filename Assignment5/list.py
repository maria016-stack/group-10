subjects = ['Math', 'Physics', 'Chemistry', 'Biology', 'CS']
print(subjects)
subjects.append('English') #adds English subject on the end of the list
print(subjects)
subjects.insert(1,'History')# adds History subject on the index 1 position
print(subjects)
subjects.remove('Biology')# it removes biology subject from the list
print(subjects)
subjects.pop()# subject removes the last subject on the list
print(subjects)
subjects.sort()# Arrange the subjects Alphabetically starting with the upper case then lower case
print(subjects)
subjects.reverse()# it arranges subjects alphabetically but in reverse order
print(subjects)
